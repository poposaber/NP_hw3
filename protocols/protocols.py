from base.message_format import MessageFormat

class Formats:
    MESSAGE = MessageFormat({
        'message_id': str, 
        'message_type': str, 
        'data': dict
    })
    """
    message_id: the id of request, usually generated by uuid4.\n
    message_type: the type of message. See Words\n
    data: keys are parameters, values are corresponding values.\n
    """

class Words:
    class MessageType:
        REQUEST = 'request'
        RESPONSE = 'response'
        EVENT = 'event'
        HANDSHAKE = 'handshake'
        HEARTBEAT = 'heartbeat'
    class Command:
        LOGIN = 'login'
        LOGOUT = 'logout'
        REGISTER = 'register'
        EXIT = 'exit'
    class DataKeys:
        PARAMS = 'params'
        class Handshake:
            ROLE = 'role'
        class Response:
            RESPONDING_ID = 'responding_id'
            RESULT = 'result'
        class Request:
            COMMAND = 'command'
    class ParamKeys:
        class Login:
            USERNAME = 'username'
            PASSWORD = 'password'
        class Logout:
            USERNAME = 'username'
        class Register:
            USERNAME = 'username'
            PASSWORD = 'password'
        class Failure:
            REASON = 'reason'
    class Roles:
        PLAYER = 'player'
        LOBBYSERVER = 'lobbyserver'
        DEVELOPERSERVER = 'developerserver'
    class Result:
        SUCCESS = 'success'
        FAILURE = 'failure'
        